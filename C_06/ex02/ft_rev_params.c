#include <unistd.h>

int	main(int ac, char **argv)
{
	int	i;
	int	j;

	i = ac - 1;
	j = 0;
	while (i > 0)
	{
		while (argv[i][j] != '\0')
		{
			write(1, &argv[i][j], 1);
			j++;
		}
		i--;
		j = 0;
		write(1, "\n", 1);
	}
}
